public class Shape {
    private double perimeter;
    private double area;
    
    public Shape() {
        this.perimeter = 0.0;
        this.area = 0.0;
    }

    public double getPerimeter() {
        return perimeter;
    }

    public void setPerimeter(double perimeter) {
        this.perimeter = perimeter;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }
    
    
}
